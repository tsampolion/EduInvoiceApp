@Composable
fun ModernProgressOverlay(
    isVisible: Boolean,
    progress: Float,
    message: String,
    onCancel: () -> Unit
) {
    if (isVisible) {
        Box(
            modifier = Modifier
                .fillMaxSize()
                .background(Color.Black.copy(alpha = 0.5f)),
            contentAlignment = Alignment.Center
        ) {
            ModernOperationProgressIndicator(
                operation = message,
                progress = progress,
                onCancel = onCancel,
                modifier = Modifier.padding(32.dp)
            )
        }
    }
}
